def get_icon(icon_name: str, size: int = 20, color: Optional[str] = None, class_name: Optional[str] = None) -> str:
    """
    Retorna HTML com SVG inline para um ícone.
    
    Args:
        icon_name: Nome do ícone (chave no dicionário ICONS)
        size: Tamanho do ícone em pixels (padrão: 20)
        color: Cor do ícone (padrão: currentColor - herda cor do texto)
        class_name: Classe CSS adicional (opcional)
    
    Returns:
        String HTML com o SVG
    
    Example:
        >>> get_icon("home", size=24)
        '<svg ...>...</svg>'
    """
    if icon_name not in ICONS:
        # Retornar ícone vazio se não encontrado
        return f'<svg xmlns="http://www.w3.org/2000/svg" width="{size}" height="{size}" viewBox="0 0 24 24"></svg>'
    
    svg = ICONS[icon_name]
    
    # Ajustar tamanho
    svg = svg.replace('width="20"', f'width="{size}"')
    svg = svg.replace('height="20"', f'height="{size}"')
    svg = svg.replace('width="24"', f'width="{size}"')
    svg = svg.replace('height="24"', f'height="{size}"')
    
    # Ajustar cor se especificada
    if color:
        svg = svg.replace('stroke="currentColor"', f'stroke="{color}"')
        svg = svg.replace('fill="currentColor"', f'fill="{color}"')
    
    # Adicionar classe CSS se especificada
    if class_name:
        # Inserir class após a tag <svg
        svg = svg.replace('<svg ', f'<svg class="{class_name}" ')
    
    return svg


def icon_text(icon_name: str, text: str, size: int = 20, spacing: int = 8, **kwargs) -> str:
    """
    Retorna HTML com ícone e texto lado a lado.
    
    Args:
        icon_name: Nome do ícone
        text: Texto a exibir
        size: Tamanho do ícone
        spacing: Espaçamento entre ícone e texto (em pixels)
        **kwargs: Argumentos adicionais para get_icon()
    
    Returns:
        String HTML com ícone e texto
    
    Example:
        >>> icon_text("home", "Início")
        '<span style="display: inline-flex; align-items: center; gap: 8px;">...</span>'
    """
    icon_html = get_icon(icon_name, size=size, **kwargs)
    return f'<span style="display: inline-flex; align-items: center; gap: {spacing}px; vertical-align: middle;">{icon_html}<span>{text}</span></span>'


def render_icon(icon_name: str, text: str = "", size: int = 20, **kwargs):
    """
    Renderiza ícone no Streamlit usando st.markdown.
    
    Args:
        icon_name: Nome do ícone
        text: Texto opcional ao lado do ícone
        size: Tamanho do ícone
        **kwargs: Argumentos adicionais para get_icon()
    
    Example:
        >>> render_icon("home", "Início")
        # Renderiza ícone + texto no Streamlit
    """
    if text:
        html = icon_text(icon_name, text, size=size, **kwargs)
    else:
        html = get_icon(icon_name, size=size, **kwargs)
    
    st.markdown(html, unsafe_allow_html=True)

